public class WhileLoopsSolution02 {

    public static void main(String[] args) {

        // Let's start with this one
        int number = 0;

        // Create a while loop here to print all numbers from 0 to 1000
        while (number <= 1000) {
            System.out.println(number);
            number = number + 1;
        }

    }

}
